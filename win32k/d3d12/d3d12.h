/*
 * MinNT - win32k/d3d12/d3d12.h
 * Self-contained Direct3D 12 interface definitions for the MinNT kernel
 * software renderer.  C-style COM (lpVtbl) only — no C++ required.
 *
 * Rules of this file:
 *   - All enum values, struct layouts, vtable method names and vtable
 *     ORDER match the official Microsoft d3d12.h / dxgiformat.h so that
 *     renderer code ported from real-world samples drops in unchanged.
 *   - Everything ntdef.h already provides (ULONG, LONG, BOOL, LUID,
 *     HANDLE, SIZE_T, FORCEINLINE, ...) is reused, never redefined.
 *   - Declarations only.  The software device implementation lives in
 *     the sibling .c files; struct-returning methods return by value,
 *     which is well-defined here because provider and consumer are both
 *     compiled by the same GCC/SysV toolchain.  (Do NOT pass these
 *     vtables across an MS-ABI boundary without adding retptr thunks —
 *     see the infamous GetCPUDescriptorHandleForHeapStart ABI story.)
 *   - GUID storage: exactly one .c file does
 *         #define D3D12_INITGUID
 *         #include "d3d12.h"
 *     to emit the IID definitions; every other includer gets externs.
 */

